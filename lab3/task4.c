//探索二维数组存储方式

#include <stdio.h>

int main() {
    int A[2][3] = {{1,2,3}, {4,5,6}};

    // 题目要求不使用指针变量，但我们可以使用取地址符 & 观察地址
    printf("A[0][0] 的内存地址: %p\n", (void*)&A[0][0]);
    printf("A[0][1] 的内存地址: %p\n", (void*)&A[0][1]);
    printf("A[1][0] 的内存地址: %p\n", (void*)&A[1][0]);

    // 观察：
    // 如果 A[0][1] 地址 = A[0][0] + 4字节 -> 按行存放
    // 如果 A[1][0] 地址 = A[0][0] + 4字节 -> 按列存放

    printf("\n实验结论：\n");
    printf("在 C 语言中，二维数组是【按行存放】的。\n");
    printf("因为 A[0][0] 之后紧跟着的是 A[0][1] 而不是 A[1][0]。\n");

    return 0;
}
