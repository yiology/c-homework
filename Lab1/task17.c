//华氏转摄氏温度
#include <stdio.h>

int main() {
    float f, c; // 使用浮点数以保证精度

    printf("请输入华氏温度：\n");
    scanf("%f", &f);

    // 公式 c = 5 * (f - 32) / 9
    // 如果写成 5/9，在C语言整数运算中结果是0，所以要写成 5.0/9.0 或先乘后除
    c = 5.0 * (f - 32.0) / 9.0;

    printf("对应的摄氏温度：\n%.2f\n", c); // %.2f 保留两位小数

    return 0;
}
